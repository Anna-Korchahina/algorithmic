function decode(expr) {
    let result = '';
    const exprLength = expr.length;

    for (let i = 0; i < exprLength; i += 10) {
        const block = expr.slice(i, i + 10);

        let space = false;
        let letter = '';
        for (let j = 0; j < 10; j += 2) {
            if (block.slice(j,j+2) === '**') {
                space = true;
                break;
            }
            letter+= conformity(block.slice(j,j+2));
        }

        if (space){
            result += ' ';
        } else {
            result += MORSE_TABLE[`${letter}`];
        }
    }

    return result;
}

